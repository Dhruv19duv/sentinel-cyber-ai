@dataclass
class TrainingExample:
    """A single training example generated from real analysis."""
    id: str
    query: str
    code_context: str
    expected_findings: List[Dict[str, Any]]
    actual_findings: List[Dict[str, Any]]
    confidence_score: float
    source_agent: str
    vulnerability_type: str
    difficulty: str  # easy, medium, hard, expert
    is_positive: bool  # True = has vuln, False = clean code
    created_at: str = field(default_factory=lambda: datetime.utcnow().isoformat())
    used_in_training: bool = False
    improvement_impact: Optional[float] = None
    model_generated: bool = False  # NEW: True if generated by real LLM

    def to_dict(self) -> Dict[str, Any]:
        return {
            "id": self.id,
            "query": self.query[:200],
            "expected_findings": len(self.expected_findings),
            "actual_findings": len(self.actual_findings),
            "confidence": self.confidence_score,
            "source": self.source_agent,
            "vuln_type": self.vulnerability_type,
            "difficulty": self.difficulty,
            "is_positive": self.is_positive,
            "used": self.used_in_training,
            "impact": self.improvement_impact,
            "model_generated": self.model_generated,
        }

    def to_training_format(self) -> Dict[str, str]:
        """Convert to chat training format for fine-tuning pipeline.
        
        Produces instruction/input/output tuples consumable by train_sentinel.py.
        """
        vuln_types = ", ".join(
            f.get("type", "unknown") for f in self.expected_findings
        )
        severity = max(
            (f.get("severity", "LOW") for f in self.expected_findings),
            key=lambda s: {"CRITICAL": 4, "HIGH": 3, "MEDIUM": 2, "LOW": 1}.get(s, 0),
        ) if self.expected_findings else "INFO"

        if self.is_positive:
            instruction = f"Find and fix the {vuln_types} vulnerability in this code."
            output = f"## Vulnerability: {vuln_types.title()}\n\n**Severity: {severity}**\n\nThe code contains a {vuln_types} vulnerability. "
            output += f"Confidence: {self.confidence_score:.0%}.\n\n"
            if self.expected_findings:
                for f in self.expected_findings:
                    if f.get("description"):
                        output += f"{f['description']}\n"
                    if f.get("remediation"):
                        output += f"\n### Fix:\n{f['remediation']}\n"
        else:
            instruction = "Review this code for security vulnerabilities."
            output = "## Security Review\n\n**Severity: INFO**\n\nNo security vulnerabilities detected in this code. The implementation follows security best practices."

        return {
            "instruction": instruction,
            "input": f"```\n{self.code_context}\n```",
            "output": output,
        }


@dataclass
class DiscoveredPattern:
    """A novel vulnerability pattern discovered through self-play."""
    id: str
    name: str
    description: str
    detection_pattern: str  # regex or code pattern
    severity: str
    cwe_mapping: Optional[str]
    discovery_source: str  # which analysis led to this
    confidence: float
    times_matched: int = 0
    false_positive_count: int = 0
    created_at: str = field(default_factory=lambda: datetime.utcnow().isoformat())

    @property
    def accuracy(self) -> float:
        total = self.times_matched + self.false_positive_count
        return self.times_matched / total if total > 0 else 0.0


